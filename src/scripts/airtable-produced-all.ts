/**
 * Uploads ALL SimpleNursing produced content to GitHub release
 * and adds every item to the Airtable "Produced Videos" table with a public link.
 *
 * Run: npx tsx src/scripts/airtable-produced-all.ts
 */
import 'dotenv/config'
import { stat } from 'fs/promises'
import { join, basename } from 'path'
import { existsSync } from 'fs'
import { spawn } from 'child_process'

const STUDIO_BASE = process.env.AIRTABLE_STUDIO_BASE ?? 'app4LEuOXBxPArLsr'
const STUDIO_KEY  = process.env.AIRTABLE_STUDIO_KEY  ?? process.env.AIRTABLE_API_KEY!
const REPO        = 'samcolibri/aistudio'
const RELEASE_TAG = 'simplenursing-assets-2026-05-01'
const TABLE_NAME  = 'Produced Videos'

const log = (msg: string) => console.log(msg)

// ── Airtable ──────────────────────────────────────────────────────────────────
async function at(path: string, method = 'GET', body?: object) {
  const res = await fetch(`https://api.airtable.com/v0${path}`, {
    method,
    headers: { Authorization: `Bearer ${STUDIO_KEY}`, 'Content-Type': 'application/json' },
    body: body ? JSON.stringify(body) : undefined,
  })
  const d = await res.json() as any
  if (!res.ok) throw new Error(`AT ${method} ${path}: ${JSON.stringify(d).slice(0, 200)}`)
  return d
}

async function getTableId(): Promise<string> {
  const meta = await at(`/meta/bases/${STUDIO_BASE}/tables`)
  const t = (meta.tables as any[]).find(t => t.name === TABLE_NAME)
  if (!t) throw new Error(`Table "${TABLE_NAME}" not found — run setup-produced.ts first`)
  return t.id
}

async function clearTable(tableId: string) {
  log('  🗑  Clearing existing records...')
  let offset: string | undefined
  do {
    const res = await at(`/${STUDIO_BASE}/${tableId}?${offset ? `offset=${offset}` : ''}`)
    const ids: string[] = res.records.map((r: any) => r.id)
    // Delete in batches of 10
    for (let i = 0; i < ids.length; i += 10) {
      const batch = ids.slice(i, i + 10)
      const qs = batch.map(id => `records[]=${id}`).join('&')
      await at(`/${STUDIO_BASE}/${tableId}?${qs}`, 'DELETE')
    }
    offset = res.offset
  } while (offset)
  log('  ✅ Table cleared')
}

async function addRecord(tableId: string, fields: Record<string, any>) {
  await at(`/${STUDIO_BASE}/${tableId}`, 'POST', { fields })
}

// ── GitHub ────────────────────────────────────────────────────────────────────
function run(cmd: string, args: string[]): Promise<string> {
  return new Promise((resolve) => {
    let out = ''
    const child = spawn(cmd, args, { stdio: 'pipe' })
    child.stdout?.on('data', d => out += d.toString())
    child.stderr?.on('data', () => {})
    child.on('close', () => resolve(out.trim()))
  })
}

async function ensureRelease() {
  const existing = await run('gh', ['release', 'view', RELEASE_TAG, '--repo', REPO, '--json', 'tagName'])
  if (existing.includes(RELEASE_TAG)) {
    log(`  ♻️  Release ${RELEASE_TAG} exists`)
    return
  }
  await run('gh', ['release', 'create', RELEASE_TAG,
    '--title', 'SimpleNursing AI Studio — All Assets',
    '--notes', 'All SimpleNursing brand assets + AI-produced content from AI Studio',
    '--repo', REPO,
  ])
  log(`  ✅ Created release ${RELEASE_TAG}`)
}

async function upload(filePath: string): Promise<string> {
  await run('gh', ['release', 'upload', RELEASE_TAG, filePath, '--clobber', '--repo', REPO])
  const name = encodeURIComponent(basename(filePath))
  return `https://github.com/${REPO}/releases/download/${RELEASE_TAG}/${name}`
}

