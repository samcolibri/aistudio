// ── Main ──────────────────────────────────────────────────────────────────────
async function main() {
  log('\n📋 Getting Airtable table...')
  const tableId = await getTableId()
  log(`  Table ID: ${tableId}`)

  log('\n🗑  Clearing old records...')
  await clearTable(tableId)

  log('\n🚀 Ensuring GitHub release exists...')
  await ensureRelease()

  log('\n📁 Cataloging all content...')
  const items = await catalog()
  log(`  Found ${items.length} items total\n`)

  let uploaded = 0
  let failed = 0

  for (const item of items) {
    const s = await stat(item.filePath).catch(() => null)
    if (!s) { log(`  ⚠ Missing: ${item.filePath}`); continue }
    const sizeMb = (s.size / 1024 / 1024).toFixed(1)

    // Upload to GitHub for public link
    let githubUrl = ''
    try {
      process.stdout.write(`  📤 [${sizeMb}MB] ${item.name.slice(0, 60)}... `)
      githubUrl = await upload(item.filePath)
      process.stdout.write('✅\n')
      uploaded++
    } catch (err) {
      process.stdout.write(`⚠ upload failed\n`)
      failed++
    }

    // Add to Airtable
    await addRecord(tableId, {
      'Name':          item.name,
      'Type':          item.category,
      'Channel':       item.channel,
      'Status':        item.status,
      'Brief ID':      item.briefId ?? '',
      'File Size':     `${sizeMb} MB`,
      'GitHub URL':    githubUrl || undefined,
      'Date Produced': new Date().toISOString().slice(0, 10),
      'Notes':         item.notes,
    }).catch(err => log(`  ⚠ Airtable failed: ${err.message?.slice(0, 80)}`))
  }

  log(`\n🎉 Done! ${uploaded} uploaded, ${failed} failed`)
  log(`\n📋 Airtable: https://airtable.com/${STUDIO_BASE}`)
  log(`🔗 GitHub:   https://github.com/${REPO}/releases/tag/${RELEASE_TAG}`)
}

main().catch(console.error)
